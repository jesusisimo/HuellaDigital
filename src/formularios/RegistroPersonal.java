    public void guardarDatos(){
        ConexionBD con=new ConexionBD();
        String clave,nombre,appaterno,apmaterno,direccion,telefono,sexo;
        clave=camp_clave.getText();
        nombre=camp_nombre.getText();
        appaterno=camp_appaterno.getText();
        apmaterno=camp_apmaterno.getText();
        direccion=camp_direccion.getText();
        telefono=camp_telefono.getText();
        sexo=camp_sexo.getText();
     try {
     //Establece los valores para la sentencia SQL
     Connection c=con.conectar(); //establece la conexion con la BD
     java.sql.Statement stmt = c.createStatement();//-insercion delusuario a la base de datos
     //stmt.executeQuery("INSERT INTO personal(nombre, huella, clave, appaterno, apmaterno, telefono, direccion, sexo) values('"+nombre+"','"+null+"','"+clave+"','"+appaterno+"','"+apmaterno+"','"+telefono+"','"+direccion+"','"+sexo+"');");
     stmt.executeQuery("INSERT INTO personal(nombre, huella, clave,appaterno,apmaterno,telefono,direccion,sexo) values('"+nombre+"','"+null+"','"+clave+"','"+appaterno+"','"+apmaterno+"','"+telefono+"','"+direccion+"','"+sexo+"');");

     stmt.execute("END");
     stmt.close();
     JOptionPane.showMessageDialog(null,"Datos del personal guardados");
     con.desconectar();
     btnGuardar.setEnabled(false);
     //btnVerificar.grabFocus();
     } catch (SQLException ex) {
     //Si ocurre un error lo indica en la consola
     System.err.println("Error al guardar los datos del personal");
     }finally{
     con.desconectar();
     }
   }

