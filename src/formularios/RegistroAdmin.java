    private void BotonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistrarActionPerformed
        // recuperando variables del frame
        String nombre_admin, contraseña_admin,ccontraseña_admin;
        nombre_admin=Camnombre.getText();
        contraseña_admin=Camcontraseña.getText();
        ccontraseña_admin=Camconfcontraseña.getText();
        ConexionBD con=new ConexionBD();
        Validaciones val=new Validaciones();
//------------valida campos y hace conexion----------------------------------------------------
        if(val.SoloLetras(nombre_admin)==true){//si el nombre contiene solo caracteres del alfabeto seguimos
            if(contraseña_admin.equals(ccontraseña_admin)){//si la contrasela es correcta continuamos
                //encriptacion de contraseña----------------------------------------------
                String contraseña_encriptada=DigestUtils.md5Hex(Camcontraseña.getText());
                //--------conexion con la base de datos para dar de alta al adminstrador----------
                try {
                Connection c=con.conectar(); //establece la conexion con la BD
                //Si la conexion fue realizada con exito, muestra el sgte mensaje.
                java.sql.Statement stmt = c.createStatement();//-insercion delusuario a la base de datos
                stmt.executeQuery("insert into administrador(nombre,contrasenia) values('"+Camnombre.getText()+"','"+contraseña_encriptada+"');");
                stmt.execute("END");
                stmt.close();
                //Cerramos la conexion------------------------------------
                JOptionPane.showMessageDialog(null,"Administrador guardado correctamente");
                con.desconectar();
                this.dispose();
                }
                catch (SQLException ex) {
                    //Si ocurre un error lo indica en la consola
                System.err.println("Error al guardar los datos del administrador.");
                }finally{
                con.desconectar();
                this.dispose();
                }
                }
            //----------------------------------------------------------------------------------------------
            else{//mensaje en caso de que no esten correctas las contraseñas
            JOptionPane.showMessageDialog(this,"Las contraseñas no coinciden");
            }
        }//--------------------------------------------------------------------------------------------------
        else{//mensaje en caso de que no este bien el nombre
        JOptionPane.showMessageDialog(this,"El nombre de administrador debe contener de 5 a 10 caracteres y no debe de contener digitos vuelva a introducir un nombre");
        }
//-----------------------------------------------------------------------------------------------------------
    }//GEN-LAST:event_BotonRegistrarActionPerformed

